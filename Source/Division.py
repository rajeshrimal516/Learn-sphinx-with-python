def Division(number_1, number_2):
    """This method will be used to divide num1 by num2

        :param int number_1: The first number also known as dividend.
        :param int number_2: The second number also known as divisor.

        :returns: The value obtained by dividing number_1 by number_2 also known as
                    quotient.
        :rtype: float
    """
    quotient = number_1 / number_2
    return quotient
