def Subtraction(number_1, number_2):
    """This method will be used to subtract number_2 from number_1.

        :param int number_1: The first number.
        :param int number_2: The second number.

        :returns: The difference between number_1 and number_2.

        :rtype: int
    """
    difference = number_1 - number_2
    return difference
